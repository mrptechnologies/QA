function displayAnniversaryDate(){
	if(document.getElementById("IsPolicyAnniversaryDateRequiredY").checked==true){
		document.getElementById("PolicyAnniversaryDateRow").style.display='inline';	
	}else{
		document.getElementById("PolicyAnniversaryDateRow").style.display='none';
	}
		
}


function displayMultipleInsuredChild(){
	if(document.getElementById("multipleInsuredY").checked==true){
		document.getElementById("multipleInsuredChild").style.display='inline';	
	}else{
		document.getElementById("multipleInsuredChild").style.display='none';
	}
		
}